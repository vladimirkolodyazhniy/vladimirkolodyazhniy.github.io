const SearchField = React.createClass({
    getInitialState() {
        return {
            name: ''
        };
    },

    handleNameChange(e) {
        this.setState({ name: e.target.value });

        this.props.onStudentSearch(e.target.value);
    },

    render() {
        const { name } = this.state;

        return (
            <div className="input-group">
                <i className="icon-search fa fa-search"/>
                <input
                    placeholder="Search student by name"
                    value={this.state.name}
                    onChange={this.handleNameChange}
                    className="form-control"
                />
            </div>
        );
    }
});

const Student = React.createClass({
    render() {
        const {
            name,
            avatar,
            location,
            email,
            github,
            facebook
        } = this.props;

        return (
            <div className="student">
                <div className="avatar-holder">
                    <img className="student-avatar" src={avatar}/>
                </div>
                <div className="content-holder">
                    <strong className="student-name">{name}</strong>
                    <strong className="student-location"><i className="fa fa-map-marker"/>{location}</strong>
                </div>
                <ul className="social-networks">
                    <li>
                        <a href={facebook} target="_blank"><i className="fa fa-facebook-square"/></a>
                    </li>
                    <li>
                        <a href={github} target="_blank"><i className="fa fa-github-square"/></a>
                    </li>
                    <li>
                        <a href={`mailto: ${email}`}><i className="fa fa-envelope-square"/></a>
                    </li>
                </ul>
            </div>
        );
    }
});

const StudentsFeed = React.createClass({
    getInitialState() {
        return {
            students: STUDENTS
        };
    },

    handleStudentSearch(name) {
        this.setState({
           students: STUDENTS.filter(student => student.name.toLowerCase().indexOf(name) !== -1)
       });
    },

    render() {
        const students = this.state.students.map(student =>
            <Student
                key={student.email}
                avatar={student.avatar}
                name={student.name}
                location={student.location}
                email={student.email}
                github={student.github}
                facebook={student.facebook}
            />
        );

        return (
            <div className="feed-holder">
                <h1>Meet React Course Members</h1>

                <SearchField onStudentSearch={this.handleStudentSearch}/>

                <div className="students-list">
                    {students}
                </div>
            </div>
        );
    }
});

ReactDOM.render(
    <StudentsFeed />,
    document.getElementById('root')
);
